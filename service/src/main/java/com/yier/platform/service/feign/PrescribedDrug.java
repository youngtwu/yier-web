package com.yier.platform.service.feign;

public class PrescribedDrug {
    //药品编码
    private String drugCode = "";
    //药品名称
    private String drugName = "";
    //药品数量
    private Integer drugCount = 0;
    //药品规格
    private String drugSpec = "";
    //剂型代码
    private String formCode = "";
    //总剂量，由于无法获取，暂时置空处理
    private String totalDose = "";
    //用药途径代码
    private String routeCode = "";
    //用药剂量
    private String dose = "";
    //剂量单位
    private String doseUnit = "";
    //用药频率
    private String rate = "";
    //频率单位
    private String rateUnit = "";

    @Override
    public String toString() {
        return "PrescribedDrug{" +
                "drugCode='" + drugCode + '\'' +
                ", drugName='" + drugName + '\'' +
                ", drugCount=" + drugCount +
                ", drugSpec='" + drugSpec + '\'' +
                ", formCode='" + formCode + '\'' +
                ", totalDose='" + totalDose + '\'' +
                ", routeCode='" + routeCode + '\'' +
                ", dose='" + dose + '\'' +
                ", doseUnit='" + doseUnit + '\'' +
                ", rate='" + rate + '\'' +
                ", rateUnit='" + rateUnit + '\'' +
                '}';
    }

    public String getDrugCode() {
        return drugCode;
    }

    public void setDrugCode(String drugCode) {
        this.drugCode = drugCode;
    }

    public String getDrugName() {
        return drugName;
    }

    public void setDrugName(String drugName) {
        this.drugName = drugName;
    }

    public Integer getDrugCount() {
        return drugCount;
    }

    public void setDrugCount(Integer drugCount) {
        this.drugCount = drugCount;
    }

    public String getDrugSpec() {
        return drugSpec;
    }

    public void setDrugSpec(String drugSpec) {
        this.drugSpec = drugSpec;
    }

    public String getFormCode() {
        return formCode;
    }

    public void setFormCode(String formCode) {
        this.formCode = formCode;
    }

    public String getTotalDose() {
        return totalDose;
    }

    public void setTotalDose(String totalDose) {
        this.totalDose = totalDose;
    }

    public String getRouteCode() {
        return routeCode;
    }

    public void setRouteCode(String routeCode) {
        this.routeCode = routeCode;
    }

    public String getDose() {
        return dose;
    }

    public void setDose(String dose) {
        this.dose = dose;
    }

    public String getDoseUnit() {
        return doseUnit;
    }

    public void setDoseUnit(String doseUnit) {
        this.doseUnit = doseUnit;
    }

    public String getRate() {
        return rate;
    }

    public void setRate(String rate) {
        this.rate = rate;
    }

    public String getRateUnit() {
        return rateUnit;
    }

    public void setRateUnit(String rateUnit) {
        this.rateUnit = rateUnit;
    }
}
